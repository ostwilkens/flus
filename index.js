export class ElementBuilder extends Function {
  constructor(elementType) {
    super('...args', 'return this._bound._call(...args)')
    this._bound = this.bind(this)
    this._bound._elementType = elementType
    this._bound._props = { style: {}, className: '' }
    this._bound._classes = []
    this.children = null

    const handler = {
      get(target, prop, receiver) {
        if (CSSProperties.includes(prop)) {
          return (value) => target.style({ [prop]: value })
        }

        return target[prop]
      }
    };

    const proxy = new Proxy(this._bound, handler)
    return proxy
  }

  _call(...args) {
    const calledAsElement = args.length === 2 && Object.keys(args[1]).length === 0

    if (calledAsElement) {
      // the function was used as a react element
      const props = args[0]

      return jsx(
        this._elementType,
        { ...this._props, ...props, style: {}, css: this._props.style }
      )

    } else {
      // called with children directly
      const children = args

      let className = this._props.className + " " + this._classes.join(" ")
      className = className.trim()
      if (className === "") {
        className = null
      }

      return jsx(
        this._elementType,
        { ...this._props, className, style: {}, css: this._props.style },
        children.length > 0 ? children : null
      )
    }
  }

  clone() {
    const newObject = new this.constructor()
    Object.assign(newObject._props, this._props)
    newObject._props.style = { ...this._props.style }
    newObject._classes = [...this._classes]
    newObject._elementType = this._elementType
    if (this.children) {
      newObject.children = [...this.children]
    }
    return newObject
  }

  with(expression) {
    const newItem = this.clone()
    expression(newItem)
    return newItem
  }

  props(props) { return this.with(x => { Object.assign(x._props, props) }) }

  // properties
  style(style) { return this.with(x => { Object.assign(x._props.style, style) }) }
  type(type) { return this.with(x => x._props.type = type) }
  href(url) { return this.with(x => x._props.href = url) }
  rel(value) { return this.with(x => x._props.rel = value) }
  target(value) { return this.with(x => x._props.target = value) }
  classes(...classes) { return this.with(x => x._classes = [...x._classes, ...classes]) }

  // callbacks
  onClick(value) { return this.with(x => x._props.onClick = value) }

  // style selectors
  hover(expression) {
    const newItem = this.clone()

    if (newItem._props.style["&:hover"] === undefined) {
      newItem._props.style["&:hover"] = {}
    }

    const target = newItem._props.style["&:hover"]

    const handler = {
      get(target, prop, receiver) {
        if (CSSProperties.includes(prop)) {
          return (value) => {
            target[prop] = value
            return receiver
          }
        }

        return target[prop]
      }
    };

    const proxy = new Proxy(target, handler)
    expression(proxy)

    return newItem
  }
}

export const el = (elementType) => new ElementBuilder(elementType)

export const Div = el("div")
export const Input = el("input")
export const A = el("a")
export const Button = el("Button")

export default { el, Div, Input, A, Button }